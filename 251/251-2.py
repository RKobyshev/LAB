import matplotlib.pyplot as plt
import numpy as np

# Исходные данные (давление в Па, температура в °C)
P = [417.3, 415.6, 413.7, 411.7, 407.8, 405.9, 402.0, 398.1, 392.2]
t = [21.1, 25.3, 30.2, 35.3, 40.2, 45.2, 50.0, 55.2, 60.2]

# Перевод температуры в Кельвины
T = np.array([x + 273.15 for x in t])

# Радиус иглы (м)
R = 0.0005

# Расчёт поверхностного натяжения sigma (Н/м) по формуле Лапласа: sigma = P * R / 2
sigma = np.array([(p * R) / 2 for p in P])

# Погрешности
sigma_err = 0.05 * sigma                     # 5% от sigma
T_err = 0.1 * np.ones_like(T)                # погрешность температуры 0.1 К

# Линейная аппроксимация зависимости σ(T) с оценкой погрешности коэффициентов
coeffs, cov = np.polyfit(T, sigma, 1, cov=True)   # polyfit с ковариационной матрицей
a, b = coeffs                                      # a = dσ/dT, b = σ при T=0 (экстраполяция)
da = np.sqrt(cov[0, 0])                            # стандартная ошибка наклона
db = np.sqrt(cov[1, 1])                            # стандартная ошибка свободного члена

# Вычисление производных величин для каждой экспериментальной точки
# Используем общий наклон a (dσ/dT) для всех точек, так как зависимость линейна
q = -T * a                                         # теплота образования единицы поверхности, Дж/м²
U_over_F = sigma - T * a                           # полная поверхностная энергия, Дж/м²

# -------------------------------------------------------------------
# Построение графиков в отдельных окнах (последовательно)

# 1. График σ(T) с погрешностями и линией регрессии
plt.figure(1, figsize=(8, 6))
plt.errorbar(T, sigma*1000, yerr=sigma_err*1000, xerr=T_err,
             fmt='o', capsize=3, capthick=1, ecolor='black',
             markersize=5, label='Экспериментальные точки')
x_fit = np.linspace(min(T), max(T), 100)
y_fit = (a * x_fit + b) * 1000
plt.plot(x_fit, y_fit, 'r-', linewidth=2,
         label=f'Линейная аппроксимация: σ = ({a:.2e})·T + ({b:.2e})')
plt.xlabel('Температура, K')
plt.ylabel('σ, мН/м')
plt.title('Зависимость поверхностного натяжения воды от температуры')
plt.legend()
plt.grid(True, linestyle='--', alpha=0.7)
plt.tight_layout()
plt.show()   # Ожидание закрытия окна

# 2. График теплоты образования единицы поверхности q(T)
plt.figure(2, figsize=(8, 6))
plt.plot(T, q*1000, 'bo-', markersize=4, label='q(T) = -T·(dσ/dT)')
plt.xlabel('Температура, K')
plt.ylabel('q, мДж/м²')
plt.title('Теплота изотермического образования единицы поверхности')
plt.grid(True, linestyle='--', alpha=0.7)
plt.legend()
plt.tight_layout()
plt.show()   # Ожидание закрытия окна

# 3. График полной поверхностной энергии U/F (T)
plt.figure(3, figsize=(8, 6))
plt.plot(T, U_over_F*1000, 'go-', markersize=4, label='U/F = σ - T·(dσ/dT)')
plt.xlabel('Температура, K')
plt.ylabel('U/F, мДж/м²')
plt.title('Полная поверхностная энергия единицы площади')
plt.grid(True, linestyle='--', alpha=0.7)
plt.legend()
plt.tight_layout()
plt.show()   # Ожидание закрытия окна

# -------------------------------------------------------------------
# Вывод результатов в консоль (после закрытия всех графиков)
print("\nРЕЗУЛЬТАТЫ ОБРАБОТКИ ДАННЫХ")
print("="*60)
print(f"Радиус иглы R = {R*1000:.2f} мм")
print(f"Температурный коэффициент dσ/dT = {a:.3e} Н/(м·К)  (погрешность ±{da:.3e})")
print(f"Свободный член b = {b:.3e} Н/м")
print("\nТаблица рассчитанных величин:")
print("-"*70)
print(f"{'T, K':>8} {'T, °C':>8} {'σ, мН/м':>10} {'q, мДж/м²':>12} {'U/F, мДж/м²':>14}")
print("-"*70)
for i in range(len(T)):
    print(f"{T[i]:8.2f} {t[i]:8.1f} {sigma[i]*1000:10.3f} {q[i]*1000:12.3f} {U_over_F[i]*1000:14.3f}")
print("="*70)