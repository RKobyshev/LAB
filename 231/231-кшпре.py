# ==================================================
# Функция обработки одного эксперимента
# ==================================================
def process_experiment(pressure_list, t_start, t_end, exp_name, color):
    """
    pressure_list: давление в единицах 10^-4 торр
    t_start, t_end: индексы (секунды) для аппроксимации
    exp_name: название для вывода
    color: цвет графика
    """
    # Массив времени (индексы = секунды)
    t_all = np.arange(len(pressure_list))
    P_all = np.array(pressure_list) * 1e-4          # перевод в торр

    # Вырезаем интервал
    mask = (t_all >= t_start) & (t_all <= t_end)
    t = t_all[mask]
    P = P_all[mask]

    # Логарифм разности (убираем точки, где P-P_pr <= 0)
    diff = P - P_pr
    valid = diff > 0
    t_valid = t[valid]
    diff_valid = diff[valid]
    ln_diff = np.log(diff_valid)

    # Линейная регрессия
    slope, intercept, r_value, p_value, std_err = stats.linregress(t_valid, ln_diff)
    tau = -1 / slope
    W = V / tau

    # Построение графика
    plt.figure(figsize=(8, 5))
    plt.plot(t_valid, ln_diff, 'o', color=color, markersize=4, label='Данные')
    fit_line = intercept + slope * t_valid
    plt.plot(t_valid, fit_line, '-', color=color, linewidth=2,
             label=f'Аппроксимация: τ = {tau:.2f} с, W = {W:.3f} л/с\nR² = {r_value**2:.4f}')
    plt.xlabel('Время, с')
    plt.ylabel('ln(P - Pₚᵣ)')
    plt.title(f'{exp_name}\nИнтервал {t_start}–{t_end} с')
    plt.grid(True)
    plt.legend()
    plt.tight_layout()
    plt.show()

    # Вывод в консоль
    print(f"\n{exp_name}")
    print(f"  Интервал: {t_start}–{t_end} с")
    print(f"  Наклон k = {slope:.5f} с⁻¹")
    print(f"  τ = {tau:.2f} с")
    print(f"  W = {W:.3f} л/с")
    print(f"  R² = {r_value**2:.4f}")

